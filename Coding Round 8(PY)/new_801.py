# Here onwards we are heading towards GUI(Graphical User Interface) 
# We have multiple options like wxPython, JPython, Tkinter, Kivy, PySlides which support GUI with help of Python 
# Here we are going to use Tkinter based GUI 


from tkinter import*

root = Tk()       # This Tk() method creates a basic root window which we see in every application we use
root.title("Note Pad")

# className is the parameter in Tk() which will change the name of the window

root.mainloop()   #This mainloop() method is used to start the application. 

# It will process the loop untill the windows is closed 
# It is an infinite loop








