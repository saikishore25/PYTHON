# Accessing the List Items 

li1 = ["rajesh", 1, True, False, 4, "5"]

print(li1[3])  # Indexing starts from 0th position 

print(li1[0:4])   # Prints list of ranged indices 

print(li1[:4])   # Prints items from 0th position to specified position 

print(li1[2:])   # Prints items from specified position to last position 



print(li1[-3])  # Negative Indexing starts from last item 

print(li1[-3:-2])  # Prints list of ranged indices 

print(li1[-4:])  # Prints items from -4 to last index (-1)

print(li1[:-1])  # Prints items from -1 to first index 

