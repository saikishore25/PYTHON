# Basic Data Types 
# 1. LIST 
# Ordered, Changeable, Allows Duplicate Values 

li1 = ["kishore", "ramesh", "rajesh"]   
li2 = [1,2,3,4,5]
li3 = ["kishore", 3, True, None]
li4 = ["kishore", 3, "kishore", "4", 3]


# Allows multiple types of Data Types in a single list 

li5 = list(("kishore", "rajesh", 1, 4, True))   # This is a List Constructor 

print(li1, type(li1))
print(li2, type(li2))
print(li3, type(li3))
print(li4, type(li4))
print(li5, type(li5))