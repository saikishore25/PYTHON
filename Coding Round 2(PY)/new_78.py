# Removing items from list 

thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)


thislist1 = ["apple", "banana", "cherry"]
thislist1.pop(1)
print(thislist1)

thislist2 = ["apple", "banana", "cherry"]
del thislist2[0]
print(thislist2)

thislist3 = ["apple", "banana", "cherry"]
thislist3.clear()
print(thislist3)

